function getRepRegiao(nome){
  if(!nome)return '';
  var nu=nome.toUpperCase().trim();
  // Exact match first
  for(var i=0;i<REPS.length;i++){if(REPS[i].nome.toUpperCase()===nu)return REPS[i].reg||'';}
  // Partial match: wrep name contains REPS nome or vice-versa
  for(var j=0;j<REPS.length;j++){
    var rn=REPS[j].nome.toUpperCase();
    if(!rn||rn==='PROJETTA PORTAS EXCLUSIVAS LTDA')continue;
    // Check if stored wrep contains the REPS name (e.g. "Simone Fraga / Deise (Tuti)" contains "SIMONE FRAGA")
    if(nu.indexOf(rn)>=0||rn.indexOf(nu)>=0)return REPS[j].reg||'';
    // Check first+last name match
    var parts=rn.split(' '),pw=nu.split(/[\s\/\(]+/);
    if(parts.length>=2&&pw.length>=2&&parts[0]===pw[0]&&pw.indexOf(parts[parts.length-1])>=0)return REPS[j].reg||'';
  }
  return '';
}
