  if(rowH) rowH.style.display = hasHoriz ? ''      : 'none';

  if(tip){
    tip.style.display = isVar ? 'block' : 'none';
    if(isVar){
      if(fv) fv.style.border = p[0]===-1 ? '2px solid #e67e22' : '';
      if(fh) fh.style.border = p[1]===-1 ? '2px solid #e67e22' : '';
    } else {
      if(fv) fv.style.border = '';
      if(fh) fh.style.border = '';
    }
  }
  // ── Toggle ripado section (modelos 08, 15, 20, 21) ──
  var _isRipMod = ['08','15','20','21'].indexOf(val) >= 0;
  var ripSec = document.getElementById('ripado-section');
  if(ripSec) ripSec.style.display = _isRipMod ? '' : 'none';
