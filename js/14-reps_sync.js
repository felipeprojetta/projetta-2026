function syncModeloToPlano(val, abrirPlan){
  // 1. carac-modelo → plan-modelo
  var pm=document.getElementById('plan-modelo');
  if(pm){
    var matched=false;
    for(var i=0;i<pm.options.length;i++){
      if(pm.options[i].value===val){pm.selectedIndex=i;matched=true;break;}
    }
    // Se não encontrou match exato, tenta prefix (ex: "01" match "01-cava")
    if(!matched){
      for(var i=0;i<pm.options.length;i++){
        if(pm.options[i].value.startsWith(val+'-')||pm.options[i].value.startsWith(val)){
          pm.selectedIndex=i;matched=true;break;
        }
      }
    }
    if(typeof planUpd==='function') planUpd();

    // 2. Abrir/expandir seção de Planificação se fechada
    if(abrirPlan && matched){
      var planBody=document.getElementById('plan-body');
      if(planBody && planBody.style.display==='none'){
        planBody.style.display='';
        var planBadge=document.getElementById('plan-badge');
        if(planBadge) planBadge.innerHTML='&#9650; clique para fechar';
      }
      // Scroll suave até a seção de Planificação
      if(planBody){
        setTimeout(function(){
          planBody.scrollIntoView({behavior:'smooth',block:'nearest'});
        },150);
      }
    }

    // 3. Feedback visual no select do plano
    if(matched && pm){
      pm.style.background='#e8f5e9';
      pm.style.borderColor='#4caf50';
      setTimeout(function(){ pm.style.background=''; pm.style.borderColor=''; },1500);
    }
  }

  // 4. Sync reverso: plan-modelo → carac-modelo (garantir consistência)
  var cm=document.getElementById('carac-modelo');
  if(cm && cm.value!==val){
    for(var i=0;i<cm.options.length;i++){
      if(cm.options[i].value===val){cm.selectedIndex=i;break;}
    }
  }
}
